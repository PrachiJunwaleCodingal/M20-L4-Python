#symmetric difference between two sets; shows all in result except common elements

set_A = {1, 2, 3, 4, 5}
set_B = {6, 7, 3, 9, 4}
result= set_A.symmetric_difference(set_B)
print("Symmetric Difference:" ,result)



set1 = {'blue', 'green'}
set2 = {'blue', 'yellow'}
result2= set1.symmetric_difference(set2)
